local requireGMForItemCommands = true


local function OnBotCommandHandlerTwo(event, player, command)
    local args = {}
    for word in command:gmatch("%w+") do table.insert(args, word) end

    if args[1] ~= "bot" or args[2] ~= "items" then
        return true
    end
	
	if requireGMForItemCommands and not player:IsGM() then
        player:SendBroadcastMessage("You do not have permission to use this command.")
        return false
    end
    

    local playerLevel = player:GetLevel()
    local level = math.floor(playerLevel / 10) * 10
    if level < 10 then level = 10 end  

    local role = args[3]
    local rarity = args[4] or ""

    if playerLevel == 80 then
        if rarity ~= "epic" and rarity ~= "rare" then
            player:SendBroadcastMessage("At level 80, you must specify either 'epic' or 'rare'. Usage: .bot items [role] [rarity]. Valid roles are 'caster', 'physical', 'tank'.")
            return false
        end
        level = "80_"..rarity
    end

    local itemList = nil

    if role == "caster" then
        itemList = CasterItems[level]
    elseif role == "physical" then
        itemList = PhysicalItems[level]
    elseif role == "tank" then
        itemList = TankItems[level]
    else
        player:SendBroadcastMessage("Invalid role specified. Usage: .bot items [role]. Valid roles are 'caster', 'physical', 'tank'.")
        return false
    end

    if not itemList then
        player:SendBroadcastMessage("No items found for the given criteria. Usage: .bot items [role]. Valid roles are 'caster', 'physical', 'tank'.")
        return false
    end

    for _, itemID in ipairs(itemList) do
        player:AddItem(itemID, 1)
    end

    player:SendBroadcastMessage("Items successfully added.")
    return false
end

RegisterPlayerEvent(42, OnBotCommandHandlerTwo)
